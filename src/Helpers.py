
class Colors:
	@classmethod
	def blue(cls): return [0, 0, 255]

	@classmethod
	def green(cls): return [0, 255, 0]

	@classmethod
	def red(cls): return [255, 0, 0]

	@classmethod
	def purple(cls): return [255, 0, 100]

	@classmethod
	def cian(cls): return [255, 200, 0]

	@classmethod
	def pink(cls): return [156, 127, 254]
